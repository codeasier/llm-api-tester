function replaceParams(template: string, params?: Record<string, string | number>) {
  if (!params) return template;
  return Object.entries(params).reduce(
    (result, [key, value]) => result.split(`{${key}}`).join(String(value)),
    template,
  );
}

function getByPath(source: TranslationTree, path: string): string {
  const value = path.split(".").reduce<unknown>((current, part) => {
    if (current && typeof current === "object") {
      return (current as Record<string, unknown>)[part];
    }
    return undefined;
  }, source);

  return typeof value === "string" ? value : path;
}

export function useI18n() {
  const locale = useAppStore((state) => state.locale);
  const setLocale = useAppStore((state) => state.setLocale);

  const t = (path: string, params?: Record<string, string | number>) => {
    const template = getByPath(messages[locale], path);
    return replaceParams(template, params);
  };

  return {
    locale,
    setLocale,
    messages: messages[locale],
    t,
  };
}

export function resolveTheme(mode: ThemeMode, prefersDark: boolean) {
  if (mode === "system") {
    return prefersDark ? "dark" : "light";
  }
  return mode;
}
